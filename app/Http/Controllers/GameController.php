<?php

namespace App\Http\Controllers;

use App\Models\Game;
use Illuminate\Http\Request;

class GameController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $games = Game::orderBy('id','DESC')->get();
        return view('admin.games.index', compact('games'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('admin.games.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'title' => 'required|string',
            'description' => 'nullable|string',
            'image' => 'required|image|mimes:webp,jpeg,png,jpg',
        ]);
        $game = new Game;
        $game->title = $request->title;
        $game->description = is_null($request->description) ? '' : $request->description;
        if ($request->hasfile('image')) {
            $image = microtime() . '-' . $request->image->getClientOriginalName();
            $request->image->move(public_path('uploads/games'), $image);
            $game->image = 'uploads/games/' . $image;
        }
        $game->save();
        return redirect()->route('games.index')->with('success','Game Added Sucessfully.');
    }

